public class tiposPrimitivos{
    public static void main(String[] args) {
        byte Byte = 10; //inteiros//
        short Short = 100;
        int Int = 2;
        long Long =  1000l;

        float Float = 0.5f; //ponto flutuante//
        double Double = 0.5;

        char Char = 'A'; //caractere//

        boolean Boolean = true; //lógico//

        System.out.println("Byte: " + Byte + 
            "\nShort: " + Short + 
            "\nInt: " + Int + 
            "\nLong: " + Long + 
            "\nFloat: " + Float + 
            "\nDouble: " + Double + 
            "\nChar: " + Char + 
            "\nBoolean: " + Boolean
        );
    }
}
